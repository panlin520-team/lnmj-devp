package com.lnmj.common.Enum;

import com.lnmj.common.Enum.base.BaseEnum;

/**
 * @Author: yilihua
 * @Date: 2019/9/6 16:14
 * @Description: 出库方式
 */
public enum OutStorageTypeEnum implements BaseEnum {

    MARKET_OUTSTORAGE(1, "销售出库"),
    OTHER_OUTSTORAGE(2, "其他出库"),
    ;

    private Integer code;
    private String desc;

    OutStorageTypeEnum(Integer code, String desc) {
        this.code = code;
        this.desc = desc;
    }

    public Integer getCode() {
        return code;
    }

    public void setCode(Integer code) {
        this.code = code;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }}
