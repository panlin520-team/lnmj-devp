public enum ResponseRoleEnum {
    ROLE_EXIT(3,"当前角色名已存在"),
    SYSTEM_ERROR(3,"系统错误"),
    DELETE_FAIL(3,"删除失败"),
    NO_RECORD(3,"无法找到该记录"),
    NO_ID(3,"无法找到该记录的id"),
    NO_CHOOSE(3,"操作失败，未获取选中记录，请重新选择"),
    COMPANY_DEFAULT_ROLE_EXIT(3,"总公司默认角色已经存在"),
    SUB_COMPANY_DEFAULT_ROLE_EXIT(3,"子公司默认角色已经存在"),
    DELETE_FAIL_NO_RECORD(3,"删除失败,无法找到该记录");

    private final int code;
    private final String desc;

    ResponseRoleEnum(int code, String desc) {
        this.code = code;
        this.desc = desc;
    }
    public int getCode(){
        return code;
    }
    public String getDesc(){
        return desc;
    }

}
