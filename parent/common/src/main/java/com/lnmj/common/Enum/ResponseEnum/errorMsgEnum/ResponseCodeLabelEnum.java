public enum ResponseCodeLabelEnum {

    ADD_FAIL(3,"添加失败"),
    UPDATE_FAIL(3,"更新失败"),
    DELETE_FAIL(3,"删除失败"),
    ;


    private final int code;
    private final String desc;

    ResponseCodeLabelEnum(int code, String desc) {
        this.code = code;
        this.desc = desc;
    }
    public int getCode(){
        return code;
    }
    public String getDesc(){
        return desc;
    }

}
