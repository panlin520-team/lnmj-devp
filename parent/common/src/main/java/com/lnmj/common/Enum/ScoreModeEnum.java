package com.lnmj.common.Enum;

import com.lnmj.common.Enum.base.BaseEnum;


public enum ScoreModeEnum implements BaseEnum {
    AUTO_SCORE(0, "自动计分"),
    MANUAL_SCORE(1, "手动计分");

    private Integer code;
    private String desc;

    public Integer getCode() {
        return code;
    }

    public void setCode(Integer code) {
        this.code = code;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    ScoreModeEnum(int code, String desc) {
        this.code = code;
        this.desc = desc;

    }


}
