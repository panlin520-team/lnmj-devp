package com.lnmj.common.Enum;

import com.lnmj.common.Enum.base.BaseEnum;

/**
 * @Author: yilihua
 * @Date: 2019/9/6 16:14
 * @Description: 业绩统计方式
 */
public enum AchievementMethodEnum implements BaseEnum {
    NUMBER_ROYALTY(1, "单个提成"),
    ALL_ACHIEVEMENT_ROYALTY(2, "总业绩提成"),
    ALL_ACHIEVEMENT_LADDER_ROYALTY(3, "总业绩阶梯提成"),
    NUMBER_LADDER_ROYALTY(4, "个数阶梯提成");
    private Integer code;
    private String desc;

    AchievementMethodEnum(Integer code, String desc) {
        this.code = code;
        this.desc = desc;
    }

    public Integer getCode() {
        return code;
    }

    public void setCode(Integer code) {
        this.code = code;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }}
