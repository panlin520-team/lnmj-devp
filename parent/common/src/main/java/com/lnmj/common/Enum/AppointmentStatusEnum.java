package com.lnmj.common.Enum;

import com.lnmj.common.Enum.base.BaseEnum;

/**
 * @Author: yilihua
 * @Date: 2019/5/14 17:24
 * @Description:  赠送状态
 */
public enum AppointmentStatusEnum implements BaseEnum {
    APPOINTMENT(1, "已预约"),
    PAY(2, "已支付"),
    CANCLE(3, "已取消"),
    OCCUPY(4, "已占用"),
    OFF(5, "已调休"),
    OVERDUE(6, "已过期"),
    ;

    private Integer code;
    private String desc;

    public Integer getCode() {
        return code;
    }

    public void setCode(Integer code) {
        this.code = code;
    }

    public String getDesc() {
        return desc;
    }

    public void setDesc(String desc) {
        this.desc = desc;
    }

    AppointmentStatusEnum(Integer code, String desc) {
        this.code = code;
        this.desc = desc;
    }

}
