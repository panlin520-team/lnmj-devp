package com.lnmj.common.Enum.ResponseEnum.errorMsgEnum;


public enum ScoreEnum {

    LIST_INFOMATION_ISNULL(3, "评分信息不存在"),
    ADD_SCORE_FAIL(3, "添加评分出错"),
    UPDATE_SCORE_FAIL(3, "更新评分出错"),
    DELETE_SCORE_FAIL(3, "删除评分出错"),
    ;

    private final Integer code;
    private final String desc;

    ScoreEnum(Integer code, String desc) {
        this.code = code;
        this.desc = desc;
    }
    public Integer getCode(){
        return code;
    }
    public String getDesc(){
        return desc;
    }

}
