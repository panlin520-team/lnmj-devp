package com.lnmj.common.Enum;


public enum LabelGradeEnum {
    HIGH_MEIRONG(1, "高端(美容)"),
    MIDDLE_MEIRONG(2, "一般(美容)"),
    LOWER_MEIRONG(3, "入门级(美容)"),
    HIGH_MEIFA(4, "高端(美发)"),
    MIDDLE_MEIFA(5, "一般(美发)"),
    LOWER_MEIFA(6, "入门级(美发)"),
    ;
    private final int code;
    private final String desc;

    LabelGradeEnum(int code, String desc) {
        this.code = code;
        this.desc = desc;
    }

    public int getCode() {
        return code;
    }

    public String getDesc() {
        return desc;
    }
}
