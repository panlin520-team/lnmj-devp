public class ArrayToString {
    public static String ArrayToString(String[] arr) {
        StringBuffer str = new StringBuffer();
        for (int i = 0; i < arr.length; i++) {
            str.append(arr[i]);
            if (i < arr.length-1) {
                str.append(",");
            }
        }
        return str.toString();
    }
}
