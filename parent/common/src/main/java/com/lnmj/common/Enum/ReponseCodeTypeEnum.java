public enum ReponseCodeTypeEnum {
    SUCCESS(1, "Success"),
    FAILURE(2, "Failure");
    private final int code;
    private final String desc;

    ReponseCodeTypeEnum(int code, String desc) {
        this.code = code;
        this.desc = desc;
    }

    public int getCode() {
        return code;
    }

    public String getDesc() {
        return desc;
    }
}
